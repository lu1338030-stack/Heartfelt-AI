# ===== 不可替换的词性(jieba pos tag 前缀) =====
# jieba 分词返回 (word, pos),pos 是英文标记
# 详见 https://github.com/fxsjy/jieba#词性标注
#
# 不可替换的词性:
#   - nrt: 名词-其他专名(人名/机构名等)
#   - nr:  名词-人名
#   - ns:  名词-地名
#   - nt:  名词-机构名
#   - nz:  其他专名
#   - m:   数词
#   - mq:  数量词
#   - t:   时间词
#   - eng: 英文单词
UNREPLACEABLE_POS_PREFIXES: frozenset[str] = frozenset(
    {"nrt", "nr", "ns", "nt", "nz", "m", "mq", "t", "eng", "x", "w"}
)
"""x = 词语-非语素词;w = 标点"""


def is_replaceable(word: str, pos: str | None = None) -> bool:
    """判断一个词是否可以替换。

    Args:
        word: jieba 分词结果
        pos: jieba 词性标注(可选)

    Returns:
        True 表示可以替换
    """
    # 1. 白名单保护
    if word in PROTECTED_TERMS:
        return False

    # 2. 词性保护
    if pos:
        # pos 可能是 "v"(动词) / "n"(名词) / "nr"(人名) 等
        # 取前 2 位作为前缀匹配,覆盖 "v","vn","n","nr","ns" 等
        prefix = pos[:2] if len(pos) >= 2 else pos
        if prefix in UNREPLACEABLE_POS_PREFIXES or pos in UNREPLACEABLE_POS_PREFIXES:
            return False

    # 3. 长度过短或过长都不替换
    # 过短(<2):可能是单字助词
    # 过长(>4):可能是复合术语
    if len(word) < 2 or len(word) > 4:
        return False

    return True


def get_synonyms(word: str) -> tuple[str, ...] | None:
    """获取一个词的同义词候选。

    Args:
        word: 待查词

    Returns:
        同义词元组(按推荐度排序),不在词典里返回 None
    """
    return SYNONYM_DICT.get(word)
