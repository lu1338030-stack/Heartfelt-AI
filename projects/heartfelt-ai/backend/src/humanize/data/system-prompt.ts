// ===== 第 4 层:个性注入(场景条件) =====

function layer4_persona(scenario: Scenario): string {
  if (scenario === 'blog') {
    return `## 个性注入策略

当前场景:**博客/随笔**

允许活泼一些:
- ✅ 口语化过渡("话说""其实""说白了")
- ✅ 轻度幽默和比喻
- ✅ 第一人称"我"的自然叙述
- ✅ 句长随机化
- ✅ 偶尔反问/设问
- ❌ 不要低俗、不要标题党`
  }

  if (scenario === 'opinion') {
    return `## 个性注入策略

当前场景:**观点文/评论**

允许鲜明立场:
- ✅ 明确的个人判断("笔者认为")
- ✅ 适度的情感色彩词
- ✅ 句长随机化 + 具体细节
- ✅ 反问/设问增加说服力
- ❌ 不要煽动性语言、不要人身攻击`
  }

  // 默认 academic
  return `## 个性注入策略

当前场景:**学术论文**

学术场景的"人味"是克制的——通过**改变句式结构 + 低频词选择**实现,不是通过扩写。

### PPL 提升三板斧(按收益排序,务必主动应用)

**① 句式重构(收益最大 +15-20,必须做)**
- 主被动转换:"研究发现X" → "X在研究中显露" / "笔者注意到X"
- 语序调整:宾语前置("这一现象,本研究观察到...")、状语后置
- 长短句拆合:把一句长句拆两句,或把两句短句并一句
- 因果倒置:先说结果再说原因(语义允许时)
- 删除显式连接词(因此/所以/由于),改用语义自然衔接

**② 低频词注入(+10-15,必须做)**
避免 AI 高频词黑名单(GPT-2 最爱预测的词,用了 PPL 就低):
| AI 高频词(避免) | 低频替代(推荐) |
|---|---|
| 研究 | 考察 / 探讨 / 审视 / 梳理 |
| 表明 | 印证 / 显露 / 透射 / 指向 |
| 重要 | 关键 / 要紧 / 核心 / 举足轻重 |
| 影响 | 波及 / 牵动 / 作用机制 / 外溢效应 |
| 发展 | 演进 / 流变 / 衍化 / 推移 |
| 问题 | 症结 / 瓶颈 / 痛点 / 隐忧 |
| 需要 | 亟须 / 离不开 / 有赖于 / 诉求 |
| 通过 | 借助 / 凭依 / 依托 / 经由 |
| 实现 | 达成 / 落地 / 促成 / 催生 |
| 提高 | 抬升 / 拉升 / 改善 / 优化 |

**③ 句长随机化(+5-10)**
- 极短句(8-12字)和长句(30-45字)交替出现
- 打破"每句都差不多长"的均匀分布(GPT-2 会发现这种规律)
- 偶尔用一个极短的判断句切入("情况并非如此。")

### 其他学术人味特征
- ✅ 适度第一人称:整段最多 1 处"笔者""本研究"(不要每句都)
- ✅ 保留原文已有的具体细节(数字、术语),不要新增
- ✅ 偶尔用模糊限定("或许""基本上""现有证据显示"),不要每句
- ✅ 偶尔的具体化("2020 年"而非"近年来",如果原文有)

### 禁忌
- ❌ 不要补充原文没有的案例或背景知识(但可以加过渡词、调整语序、展开缩写)
- ❌ 不要幽默、感叹、口语化、对话腔
- ❌ 不要"你""我"的私人叙述(用"笔者""本研究")
- ❌ 不要情感渲染、文学修辞
- ❌ 输出长度控制在原文的 90%-130%(允许适度扩写打乱 n-gram,但不要注水)
- ❌ 不要用模板句式("随着...的发展""在...方面""基于...的分析"——这些是 PPL 杀手)`
}

// ===== 第 5 层:误报防护 =====

function layer5_detection_guard(): string {
  return `## 误报防护(重要)

不要矫枉过正。以下情况**保留**,不要"修正":
- 罕见的具体细节(某年某月某实验的具体参数)→ 这是人类特征
- 混合感受的表述("虽然有效,但成本高")→ 真诚
- 自然的旁白/注释 → 像人
- 句长自然变化 → 不要强行拉平

判定 AI 痕迹的阈值:**同一段命中 3 个及以上模式**才算。1-2 个孤立特征是人类也有的,别过度改写导致语义失真。

## 输出前自检
在给出最终文本前,在你的 reasoning 里再扫一遍:
1. 破折号(— – ——)还有吗?有 → 没改完,回去改
2. 模板连接词(首先/其次/综上所述/此外/随着...的发展)还有吗?有 → 查替换表换掉
3. 每句长度都差不多吗?是 → 句长没随机化,回去打散
4. **AI 高频词黑名单**(研究/表明/重要/影响/发展/问题/需要/通过/实现/提高)你用了几个?超过 2 个 → PPL 会低,换成低频同义词
5. 句子骨架有改变吗?如果还是原文的"主谓宾"顺序 → 做主被动转换或语序调整`
}

// ===== 第 6 层:输出格式硬约束 =====

function layer6_output_contract(): string {
  return `## 输出格式(严格遵守)

只输出改写后的文本,不要任何解释、标注、前后缀。
不要输出 "改写结果:" 或 markdown 代码块包裹。
直接给改写后的段落正文。

**长度约束:输出字符数控制在原文的 90%-130%。** 允许适度扩写来打乱句式和 n-gram(格子达降重通常扩写 20-30%),但不要注水或重复。
如果你发现改写后比原文长,回去删减,直到比原文短。
宁可丢失一个修饰语,不可新增任何内容。

破折号(— – ——)在最终输出里**必须为 0**。这是硬约束。
如果你在 reasoning 里发现自己还想用破折号,改用逗号或句号。`
}

// ===== 第 8 层:内容个性化注入(Grok 维度 5) =====

/**
 * 把用户的 personalContext 编织进改写指令。
 *
 * 为空时返回空字符串(buildSystemPrompt 会过滤掉),完全不影响旧行为。
 *
 * 设计要点:
 *   - 强调"编织"而不是"拼接":不要在文末贴一段"我是 XX",而是
 *     让作者的视角渗透进每句话的措辞和侧重
 *   - 明确界限:个性化 ≠ 篡改原文论点。原文有几个论点仍几个论点,
 *     但每个论点可以带上作者的私人锚点("在我的田野观察中..."这种)
 *   - 防止过载:作者身份信息只用 1-2 处体现,不要每句都自报家门
 *   - 兼容所有 scenario:academic 时仍要克制(用"笔者""本研究"),
 *     blog/opinion 时可以更直接的第一人称
 */
function layer8_personal_context(personalContext?: string): string {
  if (!personalContext || personalContext.trim().length === 0) return ''

  // 限长防滥用(prompt 膨胀会拖慢 LLM 且增加成本)
  const ctx = personalContext.trim().slice(0, 500)

  return `## 内容个性化注入(作者视角编织,对应 Grok 6 维度方案维度 5)

以下是作者本人的背景/立场/经验,你必须在改写中体现这种个人视角:

"""
${ctx}
"""

### 编织要求(关键)

1. **渗透式表达**:不要在文末或开头贴一段自我介绍。让作者身份渗透进
   论据的措辞和侧重——例如原文说"研究表明 X",若作者做过相关田野,
   改写可以变成"X 这一现象,在笔者的观察中也清晰可见"。

2. **保留原文论点数量和核心结论**:个性化是改变"怎么说",不是改变
   "说什么"。原文 3 个论点改写后仍 3 个,结论不变,只是带上作者视角。

3. **克制使用**:全文最多 1-2 处明显的作者锚点(如"笔者的观察""本研究
   在 X 中注意到")。不要每段都自报家门,否则反而像 AI 模仿人类。

4. **场景适配**:
   - academic:用"笔者""本研究",保持正式
   - blog:可以用"我",更自然
   - opinion:明确个人判断,"笔者认为 X"

5. **作者偏好优先**:如果作者指定了写作偏好(如"用'我们看到'而非
   '研究表明'"),在不破坏学术性的前提下尽量采纳。

6. **不要捏造**:personalContext 里没有的事实,绝对不要编造进文本。
   只用作者明确给出的信息。`
}

// ===== 第 7 层:本段特定提示(动态注入) =====

function layer7_flagged_hints(hints?: string[]): string {
  if (!hints || hints.length === 0) return ''

  const hintLines = hints.map(h => `- ${h}`).join('\n')
  return `## 本段重点提示(规则引擎预处理标记)

以下是这段文本已被规则引擎标记的结构模式,你在改写时重点关注:
${hintLines}`
}
