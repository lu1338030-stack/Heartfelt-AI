export const PROMPT_VERSION = '1.2.0'

// ===== 类型 =====

export type Scenario = 'academic' | 'blog' | 'opinion'

export interface BuildPromptOptions {
  scenario: Scenario
  /** 来自规则引擎的 flagged 标记(hint 字符串数组) */
  flaggedHints?: string[]
  /**
   * 用户的个性化上下文(Grok 6 维度方案维度 5:内容个性化)
   *
   * 可选。用户提供的一段自由文本,描述作者本人的:
   *   - 身份背景("我是北师大教育学研三在读,做 K12 课堂观察")
   *   - 研究立场("倾向于批判性视角,不轻信厂商宣传")
   *   - 个人经验("我在三个乡村小学做过 6 个月田野调查")
   *   - 写作偏好("习惯用'我们看到'而不是'研究表明'")
   *
   * Layer 8 会把它编织进改写,让文本带上作者独有的视角痕迹,
   * 解决"形式都对但读起来空洞"这一 AI 文本顽疾。
   *
   * 为空时 Layer 8 不注入,行为完全等同 v1.1.1(向后兼容)。
   */
  personalContext?: string
}

// ===== 主函数 =====

export function buildSystemPrompt(opts: BuildPromptOptions): string {
  const layers = [
    layer1_role(),
    layer2_task_principles(),
    layer3_patterns(),
    layer4_persona(opts.scenario),
    layer5_detection_guard(),
    layer6_output_contract(),
    layer7_flagged_hints(opts.flaggedHints),
    layer8_personal_context(opts.personalContext),
  ].filter(l => l.length > 0) // 过滤空层

  return layers.join('\n\n---\n\n')
}

// ===== 第 1 层:角色定义 =====

function layer1_role(): string {
  return `你是一位中文学术写作润色专家,专长是把 AI 生成的文本改成像人类写的。
你的任务:接收一段预处理过的中文文本,重写它,消除所有 AI 生成痕迹,
同时保持原文的学术含义、论证逻辑和专业术语不变。

你不是翻译机,不是润色工具。你是一个"去 AI 味"的改写专家。
改写后的文本必须能通过格子达等 AI 检测平台的检测(AI 率目标 ≤ 20%)。`
}

// ===== 第 2 层:4 条任务原则 =====

function layer2_task_principles(): string {
  return `## 任务原则(不可违反)

1. **识别 AI 模式**:先在思考过程(reasoning)里列出这段文本命中的 AI 写作模式(从下方 33 种里找),逐条说明为什么命中。

2. **重写不删意**:原文有几个论点,改写后就必须有几个论点。不允许合并论点、删除论点、新增论点。
   - **长度约束:输出字符数控制在原文的 90%-130% 之间。允许适度扩写来打乱句式和 n-gram,但不要注水。**
   - 不要补充原文没有的背景、案例、解释(但可以加过渡词、调整语序、展开缩写)。
   - 原文有的具体细节(数字、术语、引用)原样保留,不要新增。

3. **保持原文的结构骨架(硬约束)**:结构层面只做小调整,主要力气花在文字表达层面。
   - 原文有什么结构,改写后就有什么结构。原文用"优势: ... 局限性: ..."分块,改写后仍要分块(具体标签词可以微调,如"优势"→"优点"、"局限性"→"短板",但不能把分块结构抹掉变成连贯叙述)。
   - 原文用"第一/第二/第三"编号,改写后仍用编号顺序(可以"其一/其二"或"首先/其次",但不能改成无序段落)。
   - 原文用分号并列多个项,改写后仍保持并列形式(分号可换顿号,但不要把并列项拆成独立段落)。
   - 原文有换行分段,改写后保持分段(可以合短段,但不要把一篇打散的结构揉成一大段,也不要把一大段硬拆成多段)。
   - 判断标准:**把原文和改写后并排放,读者一眼能看出是"同一种排版/同一种骨架",只是文字变了**。如果读者觉得"结构全变了",就是过度改写。

4. **保持含义**:专业术语(如"卷积神经网络""抗原检测")原样保留。数据、引用、公式、图表编号不动。只改"怎么说",不改"说什么"。

5. **匹配音调**:这是学术论文,所以——
   - 保持正式书面语,不口语化
   - 克制注入个性:只保留句长变化 + 适度第一人称("笔者""我们")
   - 不要幽默、不要感叹、不要对话腔

6. **PPL 困惑度意识(降 AI 核心)**:格子达等检测平台用中文 GPT-2 算困惑度(PPL),占比 40%。
   - **PPL 越高 = GPT-2 越猜不中你的下一个词 = 越像人写**。目标 PPL 42-60(稳定区间)。
   - 提升手段(按收益排序):**句式重构(+15-20) > 低频词注入(+10-15) > 句长随机化(+5-10) > 打散常用搭配(+3-8)**
   - 你要主动避免 GPT-2 能预测到的"安全词序":常用搭配、模板句式、高频词
   - 用更罕见、更不常规、但仍合语法的表达替代"最顺嘴"的写法
   - **高收益句式手段**:长难句嵌套多重从句、插入状语、倒装(用逗号或括号,不要用破折号)`
}

// ===== 第 3 层:33 种 AI 模式清单 =====

